
public class FindPositiveAndNegativeNumber {

	public static void main(String[] args) {
		double numberToBeChecked = 00;
		
		if(numberToBeChecked > 0.0) {
			System.out.println(numberToBeChecked +" number is positive");
		} else if(numberToBeChecked < 0.0) {
			System.out.println(numberToBeChecked +" number is negative");
		} else {
			System.out.println(numberToBeChecked +" number is zero");
		}

	}

}
